// if and else statements in JavaScript

let x = 15;

if(x === 10){
    console.log("Value of x is 10");
}else{
    console.log("Value of x is not 10");
}

// else if statement


let y = 5;

if(y >= 10){
    console.log("Value of y is 10");
}else if (y === 20){ 
    console.log("Value of y is 20");
}else{
    console.log("Value of y is not 10 or 20");
}